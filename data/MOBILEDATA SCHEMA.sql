CREATE TABLE MOBILEDATA (
ID SERIAL, 
COMPANY_NAME VARCHAR(30) NOT NULL, 
PHONE_NAME VARCHAR(50) NOT NULL, 
OPERATING_SYSTEM VARCHAR(30) NOT NULL, 
PHYSICAL_SIZE VARCHAR(20) NOT NULL, 
RESOLUTION VARCHAR(20) NOT NULL, 
PIXEL_DENSITY VARCHAR(10) NOT NULL, 
CAMERA VARCHAR(20) NOT NULL, 
FRONT_CAMERA VARCHAR(20) NOT NULL, 
PROCESSOR VARCHAR(20) NOT NULL, 
CLOCK_RATE VARCHAR(10) NOT NULL, 
SYSTEM_MEMORY VARCHAR(20) NOT NULL, 
BUILT_IN_STORAGE VARCHAR(10) NOT NULL, 
BATTERY_CAPACITY VARCHAR(10) NOT NULL, 
MULTIPLE_SIM_CARDS BOOLEAN NOT NULL, 
SIM_CARD VARCHAR(6) NOT NULL, 
CONSTRAINT MOBILEDATA_PK1 PRIMARY KEY (ID));